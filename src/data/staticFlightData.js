// Base date: March 15, 2024 (fixed reference point)
const FIXED_BASE_DATE = new Date('2024-03-15');

// Generate fixed selectable dates (90 days from base date)
const generateFixedDates = () => {
  const dates = [];
  for (let i = 0; i < 90; i++) {
    const date = new Date(FIXED_BASE_DATE);
    date.setDate(FIXED_BASE_DATE.getDate() + i);
    dates.push(date.toISOString().split('T')[0]); // YYYY-MM-DD format
  }
  return dates;
};

