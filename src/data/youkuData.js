export const getRecommendedVideos = (videoId, limit = 10) => {
  const currentVideo = getVideoById(videoId);
  if (!currentVideo) return videos.slice(0, limit);
  
  return videos
    .filter(v => v.id !== videoId && v.category === currentVideo.category)
    .slice(0, limit);
};

